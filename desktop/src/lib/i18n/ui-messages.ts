export type UiMessageCode = (typeof UI_MESSAGE_CODES)[number];
export type UiMessageParameter = string | number | boolean;
export type UiMessageParameters = Readonly<Record<string, UiMessageParameter>>;

export interface UiMessageDescriptor {
  code: UiMessageCode;
  params: UiMessageParameters;
}

const allowedParameterNames = new Set([
  "count",
  "deviceLabel",
  "endpoint",
  "minutes",
  "peer",
  "port",
  "spaceName",
  "version",
]);

export function uiMessage(
  code: UiMessageCode,
  params: Record<string, UiMessageParameter> = {},
): UiMessageDescriptor {
  for (const [name, value] of Object.entries(params)) {
    if (!allowedParameterNames.has(name)) {
      throw new Error(`unsafe ui message parameter: ${name}`);
    }
    if (typeof value === "string" && value.length > 160) {
      throw new Error(`unsafe ui message parameter value: ${name}`);
    }
  }
  return { code, params: Object.freeze({ ...params }) };
}

type UiMessageFormatter = (params: UiMessageParameters) => string;
type UiMessageCatalog = Record<UiMessageCode, UiMessageFormatter>;

const value = (params: UiMessageParameters, name: string): string => String(params[name] ?? "");
const count = (params: UiMessageParameters): number => Number(params.count ?? 0);

