const catalogs: Record<SupportedLocale, UiMessageCatalog> = {
  "zh-CN": zhCN,
  "en-US": enUS,
};

export function formatUiMessage(
  locale: SupportedLocale,
  descriptor: UiMessageDescriptor,
): string {
  const formatter = catalogs[locale][descriptor.code] ?? catalogs[locale]["common.unknown"];
  return formatter(descriptor.params);
}

export function unknownUiMessage(): UiMessageDescriptor {
  return uiMessage("common.unknown");
}
