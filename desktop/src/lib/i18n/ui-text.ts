const catalogs: Record<SupportedLocale, Record<UiTextKey, string>> = {
  "zh-CN": zhCNText,
  "en-US": enUSText,
};

export function text(
  locale: SupportedLocale,
  key: UiTextKey,
  parameters: UiTextParameters = {},
): string {
  return catalogs[locale][key].replace(/\{([A-Za-z][A-Za-z0-9]*)\}/g, (match, name: string) =>
    Object.prototype.hasOwnProperty.call(parameters, name) ? String(parameters[name]) : match,
  );
}

export function pluralText(
  locale: SupportedLocale,
  count: number,
  oneKey: UiTextKey,
  otherKey: UiTextKey,
  parameters: UiTextParameters = {},
): string {
  const key = new Intl.PluralRules(locale).select(count) === "one" ? oneKey : otherKey;
  return text(locale, key, { ...parameters, count });
}
