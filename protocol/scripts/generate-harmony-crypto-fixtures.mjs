import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const root = join(scriptDir, "..");
const cryptoDir = join(root, "test-vectors", "crypto");
const output = join(root, "..", "harmony", "entry", "src", "test", "ProtocolCryptoFixtures.ets");

const fixtures = [
  ["ED25519_SIGNATURE_VECTOR", "ed25519-signature.valid.json"],
  ["X25519_SHARED_SECRET_VECTOR", "x25519-shared-secret.valid.json"],
  ["HMAC_SHA256_VECTOR", "hmac-sha256.valid.json"],
  ["HKDF_SHA256_VECTOR", "hkdf-sha256.valid.json"],
  ["AES_256_GCM_VECTOR", "aes-256-gcm.valid.json"],
  ["SESSION_KEYS_VECTOR", "session-keys.valid.json"],
  ["REPLAY_COUNTER_VECTOR", "replay-counter.reject.json"],
];

const lines = [
  "// Generated by protocol/scripts/generate-harmony-crypto-fixtures.mjs.",
  "// Source of truth: protocol/test-vectors/crypto/*.json.",
  "// Do not put production keys, pairing secrets, or clipboard content here.",
  "",
];

for (const [name, fileName] of fixtures) {
  const content = readFileSync(join(cryptoDir, fileName), "utf8").trim();
  JSON.parse(content);
  lines.push(`export const ${name}: string = ${JSON.stringify(content)};`);
  lines.push("");
}

writeFileSync(output, `${lines.join("\n").trimEnd()}\n`, "utf8");
console.log(`Wrote ${output}`);
